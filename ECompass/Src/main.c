/* USER CODE BEGIN 0 */

uint8_t Data_acc[6];
uint8_t Data_mag[6];

int32_t Xaxis_acc = 0;
int32_t Yaxis_acc = 0;
int32_t Zaxis_acc = 0;
int32_t Xaxis_mag = 0;
int32_t Yaxis_mag = 0;
int32_t Zaxis_mag = 0;

float axes_aF[3];
float axes_aT[3];
float roll, pitch, x, y, h, dir;
uint16_t dirUint16_t;
uint8_t dirUint8_t[2];

/* USER CODE END 0 */

int main(void) {

	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration----------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_I2C1_Init();
	MX_USART2_UART_Init();

	/* USER CODE BEGIN 2 */

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */

		HAL_I2C_Mem_Read(&hi2c1, LSM303_MAG_ADDRESS,
		LSM303_MAG_X_L_A_MULTI_READ, 1, Data_mag, 6, HAL_MAX_DELAY);
		HAL_Delay(100);
		HAL_I2C_Mem_Read(&hi2c1, LSM303_ACC_ADDRESS,
		LSM303_ACC_X_L_A_MULTI_READ, 1, Data_acc, 6, HAL_MAX_DELAY);

		Xaxis_acc = ((Data_acc[1] << 8) | Data_acc[0]);
		Yaxis_acc = ((Data_acc[3] << 8) | Data_acc[2]);
		Zaxis_acc = ((Data_acc[5] << 8) | Data_acc[4]);

		//Taking sensitivity into account
		Xaxis_mag = (((Data_mag[1] << 8) | Data_mag[0])) * 1.5;
		Yaxis_mag = (((Data_mag[3] << 8) | Data_mag[2])) * 1.5;
		Zaxis_mag = (((Data_mag[5] << 8) | Data_mag[4])) * 1.5;

		axes_aF[0] = (float) Xaxis_acc;
		axes_aF[1] = (float) Yaxis_acc;
		axes_aF[2] = (float) Zaxis_acc;

		// Resolution is 2^(8) = 256
		axes_aT[0] = (axes_aF[0] * 2 / 256);
		axes_aT[1] = (axes_aF[1] * 2 / 256);
		axes_aT[2] = (axes_aF[2] * 2 / 256);

		// Some math to get the thing working
		roll = atan2f(axes_aT[1], axes_aT[2]);
		pitch = atan2f(-axes_aT[0],
				axes_aT[1] * sinf(roll) + axes_aT[2] * cosf(roll));

		x = Xaxis_mag * cosf(pitch)
				+ (Xaxis_mag * sinf(roll) + Yaxis_mag * cosf(roll))
						* sinf(pitch);
		y = Yaxis_mag * sinf(roll) - Xaxis_mag * cosf(roll);

		// Convert to degrees, look in what quadrant the value lays and if it's
		// negative add 360° to achieve correct positioning.
		h = atan2f(y, x);
		dir = h * (180 / 3.1415);
		if (dir < 0)
			dir += 360;

		// Convert for transmission over DASH7
		dirUint16_t = (uint16_t) dir;
		dirUint8_t[1] = dirUint16_t & 0xff;
		dirUint8_t[2] = (dirUint16_t) >> 8;

	}
	/* USER CODE END 3 */

}

/** System Clock Configuration
 */
void SystemClock_Config(void) {

	RCC_OscInitTypeDef RCC_OscInitStruct;
	RCC_ClkInitTypeDef RCC_ClkInitStruct;

	/**Configure the main internal regulator output voltage
	 */
	__HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

	/**Initializes the CPU, AHB and APB busses clocks
	 */
	RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
	RCC_OscInitStruct.HSIState = RCC_HSI_ON;
	RCC_OscInitStruct.HSICalibrationValue = 16;
	RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
	RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;
	RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL6;
	RCC_OscInitStruct.PLL.PLLDIV = RCC_PLL_DIV3;
	if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK) {
		_Error_Handler(__FILE__, __LINE__);
	}

	/**Initializes the CPU, AHB and APB busses clocks
	 */
	RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_SYSCLK
			| RCC_CLOCKTYPE_PCLK1 | RCC_CLOCKTYPE_PCLK2;
	RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
	RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
	RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
	RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

	if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_1) != HAL_OK) {
		_Error_Handler(__FILE__, __LINE__);
	}

	/**Configure the Systick interrupt time
	 */
	HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq() / 1000);

	/**Configure the Systick
	 */
	HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

	/* SysTick_IRQn interrupt configuration */
	HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

